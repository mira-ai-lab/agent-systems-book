"""
任务分解示例 - Task Decomposition Demo

用户 Query + Agent 能力 → Prompt → LLM → 子任务列表
"""

import os
import httpx
from dotenv import load_dotenv
from langchain_core.messages import HumanMessage
from langchain_core.prompts import PromptTemplate
from langchain_openai import ChatOpenAI

load_dotenv()

# 示例输入
QUERY = (
    "我想带父母和孩子去北京玩 3 天 2 晚，预算 8000 元以内。希望去故宫、长城和一个适合孩子的科技类场馆。父母不适合长时间步行，每天步行最好不要超过 8000 步。如果遇到下雨，希望自动调整为室内项目。请给出每天的行程安排、交通建议、预算估计和备选方案"
)

# Agent 团队能力（任务分解时只需自然语言描述）
AGENT_TEAM = """ 根据用户输入的城市和具体日期查询天气预报。
根据用户输入的城市推荐酒店及住宿。
根据用户输入的地点推荐餐厅。
根据用户提供的出发地、目的地、天数等信息规划综合旅行行程，涵盖交通方式和必去景点等方面。
根据具体日期、出发城市和到达城市查询航班信息。
根据用户输入的新闻标题搜索相关新闻内容。"""

TASK_DECOMPOSE_PROMPT = """
##角色：你是一个高度熟练的任务拆解专家，专注于将用户输入拆解为粒度适当的子任务。
##任务：根据用户的输入，分析目标以及智能体团队中所有智能体的能力，通过将用户输入分解为智能体团体能力能够覆盖的信息明确的子任务，以帮助有效地实现用户的目标。
##背景信息（关于具体任务拆解所需要知道的相关信息及要求）：
    {background_info}
##智能体团队（可用于完成子任务的所有智能体能力）：
    {agent_team}
##步骤：
    1. **理解目标**：根据所有用户输入确定用户想要实现的核心目标和结果。
    2. **考虑背景信息**：提供的背景信息中包含拆解任务所需要的额外的领域知识，可用作参考。
    3. **定义子任务**：根据目标的复杂程度将任务拆解为子任务。
      3.1 **任务拆解原则**：
        - 简单：每个子任务都是一个能够匹配智能体团队能力的单一且信息明确的祈使句指令。
        - 信息完整：每个子任务指令中必须包含明确的槽位信息，所需槽位信息从用户输入及目标中抽取。
        - 优化复杂度：根据任务复杂度调整子任务数量，使用最少子任务完成整体目标。
    4. **确认子任务能否完成**：判断拆解出的每个子任务是否在智能体团队能力范围内，如不在该子任务为'NULL'。
    5. **补充子任务信息**：针对每个子任务，提取该子任务的所有槽位，并根据目标补全所有槽位，合并成一句完整的祈使指令。
##要求：
    4.仅输出简洁且信息完整的整体目标内容以及槽位信息完整的任务拆解结果，不要输出分析过程及原因;
##输出格式：
    # 目标
        整体目标内容（不包含人称的祈使句式）
    # 任务拆解
     - 子任务1
     - 子任务2(如有)
##用户输入：
    {query}
"""

template = PromptTemplate.from_template(TASK_DECOMPOSE_PROMPT)
full_prompt = template.format(background_info="", agent_team=AGENT_TEAM, query=QUERY)

print("=" * 70)
print("【发给大模型的完整提示词】\n")
print(full_prompt)
print("\n" + "-" * 70)

llm = ChatOpenAI(
    model=os.getenv("DASHSCOPE_CHAT_MODEL", "qwen3.6-plus"),
    temperature=0,
    base_url=os.getenv(
        "DASHSCOPE_CHAT_BASE_URL",
        "https://dashscope.aliyuncs.com/compatible-mode/v1",
    ).rstrip("/"),
    api_key=os.getenv("DASHSCOPE_API_KEY"),
    http_client=httpx.Client(verify=False),
)

print("\n【模型输出：任务分解】\n")
response = llm.invoke([HumanMessage(content=full_prompt)])
print(response.content)

