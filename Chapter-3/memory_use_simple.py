"""
记忆使用示例 - Memory Use（Prompt 拼接）

演示如何将检索到的记忆融入 Prompt，引导大模型结合记忆回答问题。
"""

from langchain_core.prompts import PromptTemplate
from langchain_core.messages import HumanMessage
from langchain_openai import ChatOpenAI
import httpx
import os
from dotenv import load_dotenv

# 加载环境变量
load_dotenv()

# ---------------------------------------------------------------------------
# 1. 提示词模板：给模型一个“作弊小抄”
# ---------------------------------------------------------------------------
MEMORY_USE_PROMPT = """你是一个有记忆的智能助手。请结合【相关长期记忆】回答用户问题。

【相关长期记忆】
{memory_context}

【当前问题】
{query}

请根据记忆中的偏好和约束给出建议。
"""

# ---------------------------------------------------------------------------
# 2. 模拟检索结果与用户提问
# ---------------------------------------------------------------------------
retrieved_memories = [
    "- [住宿] 用户喜欢住海景房，预算每晚不超过800元。",
    "- [饮食] 用户是素食主义者，不吃海鲜。"
]

memory_context = "\n".join(retrieved_memories)
user_query = "帮我推荐一下三亚的旅行方案吧，我想住得舒服点。"

# ---------------------------------------------------------------------------
# 3. 配置并调用大语言模型
# ---------------------------------------------------------------------------
llm = ChatOpenAI(
    model=os.getenv("DASHSCOPE_CHAT_MODEL", "qwen-plus"),
    temperature=0,
    base_url=os.getenv("DASHSCOPE_CHAT_BASE_URL", "https://dashscope.aliyuncs.com/compatible-mode/v1"),
    api_key=os.getenv("DASHSCOPE_API_KEY"),
    http_client=httpx.Client(verify=False)
)

# 生成完整提示词
use_template = PromptTemplate.from_template(MEMORY_USE_PROMPT)
full_prompt = use_template.format(
    memory_context=memory_context,
    query=user_query
)

print("=" * 70)
print("【发给大模型的完整提示词】")
print("-" * 70)
print(full_prompt)
print("\n" + "-" * 70)

# 发送请求并获取响应
print("【模型回答：结合了记忆的个性化建议】\n")
response = llm.invoke([HumanMessage(content=full_prompt)])
print(response.content)

print("\n" + "=" * 70)
print("演示结束。这就是‘个性化 AI’的实现逻辑。")

